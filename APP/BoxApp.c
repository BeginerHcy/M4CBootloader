void cyc100ms(){
	
	if(CNTFlash==0||CNTFlash==2){
		gMachineIO.LED = 1;
		CNTFlash++;
	}
	else if(CNTFlash==1||CNTFlash==3){
		gMachineIO.LED = 0;
		CNTFlash++;
	}	
	else if (CNTFlash>10){
		CNTFlash = 0;
	}
	else{
		CNTFlash++;
	}	
}
